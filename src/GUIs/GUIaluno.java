    private void pesqAlunoBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesqAlunoBTActionPerformed
        String pk2 = RATF.getText();
        DAOaluno daOaluno = new DAOaluno();
        

        Aluno aluno = new Aluno();
        aluno = daOaluno.consultar(Integer.valueOf(pk2));

        if (null == aluno) {
            tabelaAluno.setVisible(false);
            JOptionPane.showMessageDialog(null, "Esse aluno não existe, adicicone ele!");
            nomeAlunoTF.setText("");
            CursoSelect.setText("");
            emailAlunoTF.setText("");
            telefoneTF.setText("");
           

        } else {
            tabelaAluno.setVisible(false);
            nomeAlunoTF.setText(aluno.getNome());
            emailAlunoTF.setText(aluno.getEmail());
            telefoneTF.setText(aluno.getTelefone());
            String pk = aluno.getCursoNomeCurso().getNomeCurso();
            CursoSelect.setText(pk);
            
            
        }

        nomeAlunoTF.setEditable(false);
        emailAlunoTF.setEditable(false);
        telefoneTF.setEditable(false);
        comboCurso.setEnabled(false);
        CursoSelect.setEditable(false);
       
    }//GEN-LAST:event_pesqAlunoBTActionPerformed

