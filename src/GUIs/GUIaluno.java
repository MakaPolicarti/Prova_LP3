    private void saveAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAlunoActionPerformed
        // função salvar

        String pk2 = RATF.getText();
        DAOcurso daOcurso = new DAOcurso();
        DAOaluno daOaluno = new DAOaluno();
        DefaultTableModel model;
        model = (DefaultTableModel) tabelaAluno.getModel();
        int ra = Integer.valueOf(pk2);

        Aluno aluno = new Aluno();

        String nome = nomeAlunoTF.getText();
        String email = emailAlunoTF.getText();
        String tel = telefoneTF.getText();
        aluno = daOaluno.consultar(ra);
        String pk = String.valueOf(comboCurso.getSelectedItem());
        Curso curso = new Curso();
       

        if (null == curso) {
            JOptionPane.showMessageDialog(null, "Esse curso não existe, adicicone ele primeiro!");
            nomeAlunoTF.setEditable(false);
            emailAlunoTF.setEditable(false);
            telefoneTF.setEditable(false);
            comboCurso.setEditable(false);
            CursoSelect.setEditable(false);
            nomeAlunoTF.setText("");
            emailAlunoTF.setText("");
            telefoneTF.setText("");
            
            RATF.setEditable(true);

        } else {
            if (null == aluno) {
                Aluno aluno2 = new Aluno();
                aluno2.setNome(nome);
                aluno2.setEmail(email);
                aluno2.setCursoNomeCurso(daOcurso.consultar(pk));
                CursoSelect.setText(pk);
                aluno2.setTelefone(tel);
                aluno2.setRa(ra);

                daOaluno.criar(aluno2);

            } else {
                aluno.setNome(nome);
                aluno.setEmail(email);
                aluno.setCursoNomeCurso(daOcurso.consultar(pk));
                aluno.setTelefone(tel);
                CursoSelect.setText(pk);
                daOaluno.atualizar(aluno);

            }

            nomeAlunoTF.setEditable(false);
            emailAlunoTF.setEditable(false);
            telefoneTF.setEditable(false);
            comboCurso.setEnabled(false);
            CursoSelect.setEditable(false);
            RATF.setEditable(true);

        }
        tabelaAluno.setVisible(false);

    }//GEN-LAST:event_saveAlunoActionPerformed

