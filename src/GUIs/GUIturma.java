    private void listTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listTurmaActionPerformed
        DAOturma daOturma = new DAOturma();
        DefaultTableModel model;
        model = (DefaultTableModel) tabelaTurma.getModel();

        List<Turma> lista2 = daOturma.findall();
        try{
        for(int n=0;n<1000;n++){
            
            model.removeRow(n);
        }
        }catch(Exception e){
            
        }
        lista2.forEach(turma -> {
            model.insertRow(model.getRowCount(), new Object[]{turma.getCodigo(),turma.getPeriodo(),turma.getCursoNomeCurso().getNomeCurso()});
        });
        tabelaTurma.setVisible(true);
    }//GEN-LAST:event_listTurmaActionPerformed

    private void pesqTurmaBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesqTurmaBTActionPerformed
        String pk3 = codTurmaTF.getText();
        DAOturma daOturma = new DAOturma();

        Turma turma = new Turma();
        turma = daOturma.consultar(pk3);

        if (null == turma) {
            tabelaTurma.setVisible(false);
            JOptionPane.showMessageDialog(null, "Essa turma não existe, adicicone ela!");
            periodoTF.setText("");

            CursoSelect.setText("");

        } else {
            tabelaTurma.setVisible(false);
            periodoTF.setText(String.valueOf(turma.getPeriodo()));
            String pk = turma.getCursoNomeCurso().getNomeCurso();
            CursoSelect.setText(pk);
        }

        periodoTF.setEditable(false);
        comboCurso.setEnabled(false);
        CursoSelect.setEditable(false);
    }//GEN-LAST:event_pesqTurmaBTActionPerformed

