    private void saveTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveTurmaActionPerformed
        String pk2 = codTurmaTF.getText();
        DAOcurso daOcurso = new DAOcurso();
        DAOturma daOturma = new DAOturma();
        DefaultTableModel model;
        model = (DefaultTableModel) tabelaTurma.getModel();

        Turma turma = new Turma();

        String periodo = periodoTF.getText();
        
        turma = daOturma.consultar(pk2);
        String pk = String.valueOf(comboCurso.getSelectedItem());
        Curso curso = new Curso();
        curso = daOcurso.consultar(pk);

        if (null == curso) {
            JOptionPane.showMessageDialog(null, "Esse curso não existe, adicicone ele primeiro!");
            periodoTF.setEditable(false);
            CursoSelect.setEditable(false);
            comboCurso.setEditable(false);
            periodoTF.setText("");
            CursoSelect.setText("");
            codTurmaTF.setEditable(true);

        } else {
            if (null == turma) {
                Turma aluno2 = new Turma();
                aluno2.setPeriodo(Integer.valueOf(periodo));
                aluno2.setCursoNomeCurso(curso);
                aluno2.setCodigo(pk2);
                CursoSelect.setText(pk);
                daOturma.criar(aluno2);

            } else {
                turma.setPeriodo(Integer.valueOf(periodo));
                turma.setCursoNomeCurso(curso);
                CursoSelect.setText(pk);
                daOturma.atualizar(turma);

            }

            periodoTF.setEditable(false);
            CursoSelect.setEditable(false);
            codTurmaTF.setEditable(true);
            comboCurso.setEnabled(false);

        }
        tabelaTurma.setVisible(false);
    }//GEN-LAST:event_saveTurmaActionPerformed

